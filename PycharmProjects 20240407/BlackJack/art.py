def new_deck(x):
    deck_n = []
    for n in range(1, 14):
        for s in range(1, 5):
            deck_n += [n + s / 10]
    deck_n *= x
    shuffle(deck_n)
    return deck_n


def new_board():
    x = []
    for n in range(1, 21):
        for s in range(1, 6):
            x += [n + s / 10]
    return x


def next_spot(s):
    t = [s]
    t += [int(s) - 1]
    t += [round((s - int(s)) * 10)]
    t += [t[1] * 5 + t[2] * 500 + 101]
    t += [t[3] + 4]
    return t


def top_card(x):
    t = [x]
    t += [int(x) - 1]
    t += [round((x - int(x)) * 10)]
    t += [t[1] * 5 + t[2] * 500 - 400]
    t += [t[3] + 4]
    return t


def number_of_players():
    players = input("How many players?")
    if players.isnumeric():
        if 0 < int(players) < 10:
            return int(players)
    number_of_players()


def the_players(a, c=[], d=0):
    while a - d:
        b = input(f"Enter name for player {d + 1}: ")
        if b.isalpha():
            b += "         "
            c += [[d * 10 + 1, d * 10 + 8, b[0].upper() + b[1:7].lower()]]
            c[d] += [d * 10 + 101, d * 10 + 104, "500"]
            d += 1
    return c
